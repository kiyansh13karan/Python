# Relational operators 
'''
Relational operators are used to evalute conditions inside the if statements.
Some examples of relational operators are :-
| Operator | Description           | Example  |
| -------- | --------------------- | -------- |
| `==`     | Equal to              | `a == b` |
| `!=`     | Not equal to          | `a != b` |
| `>`      | Greater than          | `a > b`  |
| `<`      | Less than             | `a < b`  |
| `>=`     | Greater than or equal | `a >= b` |
| `<=`     | Less than or equal    | `a <= b` |
'''

#examples of relational operators

a = 10
b = 20
print("a == b:", a == b)    # False
print("a != b:", a != b)    # True
print("a > b:", a > b)      # False
print("a < b:", a < b)      # True
print("a >= b:", a >= b)    # False
print("a <= b:", a <= b)    # True
