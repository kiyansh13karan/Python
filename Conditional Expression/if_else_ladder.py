# if else conditions of even odd testing 
num = int(input("Enter a number : ")) 

if(num % 2 == 0) :
    print("Number is even")
else :
    print("Number is Odd") 




# Check if a number is positive or negative
num = int(input("Enter a number: "))

if num >= 0:
    print("The number is positive.")
else:
    print("The number is negative.")





